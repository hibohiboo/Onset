                  <p><h3>Onset!の使い方</h3></p>
                        <p>
                              まず､ログインしたい部屋を部屋一覧から選択します<br>
                              無いなら建てましょう<br>
                              あとは名前と部屋のパスワードを入力してログイン<br>
                              これだけです<br>
                              <i>パスワードは必須です(無用なトラブルを防ぐため､パス無しの部屋は建てられません)</i>
                        </p>

                        <p>
                              部屋に入ると､白い入力欄が2つあって､その下に他の人の発言があると思います<br>
                              (あなたが最初にログインしたなら､他の人の発言は無いかもしれませんが)<br><br>
                              上の欄は名前です<br>
                              自由に変えられますが､なりすましの心配はありません<br>
                              名前の横の英数字があなたのIDで､これがある限りなりすましは不可能です<br>
                              (まれに被ることがあるので､その時はログインしなおしてください､もっとも､Onset!の乱数が被るのは(ダイスも含めて)1/2<sup>19937</sup>-1というごくごく稀にしか起こりません)<br><br>
                              名前欄の下はテキスト欄です<br>
                              テキスト欄になにか打ち込んで､送信ボタンを押すと､その下のチャット欄に反映されます<br>
                              また､ここにダイスコマンドを打ち込むと､ダイスを振ることができます<br><br>
                              テキスト欄の内容は1秒毎に更新され､他の人が喋った内容がリアルタイムに反映されます
                        </p>

